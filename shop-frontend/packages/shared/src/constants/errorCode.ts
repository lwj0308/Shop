/**
 * 根据错误码获取中文提示
 * 优先从映射表查找，找不到则返回通用提示
 * @param code - 后端返回的错误码
 * @param defaultMessage - 默认提示信息
 * @returns 中文错误提示
 */
export function getErrorMessage(code: number, defaultMessage?: string): string {
  return ERROR_CODE_MAP[code] || defaultMessage || '操作失败，请稍后重试'
}
